        /**
         * Auto-generates a unique customer ID based on the existing maximum ID in the
         * database.
         * Sets the generated ID to the txtid JTextField.
         *
         * @throws ClassNotFoundException if the MySQL JDBC driver class is not found.
         * @throws SQLException           if a SQL exception occurs while querying the
         *                                database.
         */
        public void autoID() {
                try {
                        con = connectionManager.getConnection();
                        Statement s = con.createStatement();
                        ResultSet rs = s.executeQuery("select MAX(id) from customer");
                        rs.next();
                        rs.getString("MAX(id)");
                        if (rs.getString("MAX(id)") == null) {
                                IdText.setText("CS001");
                        } else {
                                long id = Long.parseLong(
                                                rs.getString("MAX(id)").substring(2, rs.getString("MAX(id)").length()));
                                id++;
                                IdText.setText("CS" + String.format("%03d", id));
                        }

                } catch (SQLException ex) {
                        Logger.getLogger(addCustomer.class.getName()).log(Level.SEVERE, null, ex);
                }
        }

