        /**
         * 
         * /**
         * Handles the action event when the user selects an image file using
         * JFileChooser.
         * Displays the selected image in the txtphoto JLabel and converts the image to
         * a byte array.
         *
         * @param evt the ActionEvent triggered by selecting an image file.
         * @throws IOException if an I/O exception occurs while reading the image file.
         */
        public void browseBtnAction(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_browseBtnAction
                // TODO add your handling code here:

                try {
                        JFileChooser picchooser = new JFileChooser();
                        picchooser.showOpenDialog(null);
                        File pic = picchooser.getSelectedFile();
                        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "png", "jpg");
                        picchooser.addChoosableFileFilter(filter);
                        path = pic.getAbsolutePath();
                        BufferedImage img;
                        img = ImageIO.read(picchooser.getSelectedFile());
                        ImageIcon imageIcon = new ImageIcon(
                                        new ImageIcon(img).getImage().getScaledInstance(250, 250, Image.SCALE_DEFAULT));
                        photoLabel.setIcon(imageIcon);

                        File image = new File(path);
                        FileInputStream fis = new FileInputStream(image);
                        ByteArrayOutputStream baos = new ByteArrayOutputStream();
                        byte[] buff = new byte[1024];
                        for (int readNum; (readNum = fis.read(buff)) != -1;) {
                                baos.write(buff, 0, readNum);
                        }
                        userimage = baos.toByteArray();
                } catch (IOException ex) {
                        Logger.getLogger(addCustomer.class.getName()).log(Level.SEVERE, null, ex);
                }
        }// GEN-LAST:event_browseBtnAction

        /**
         * Handles the action event when the user clicks the "Add Customer" button.
         * Validates input data and inserts customer details into the database.
         *
         * @param evt the ActionEvent triggered by clicking the "Add Customer" button.
         * @throws ClassNotFoundException if the MySQL JDBC driver class is not found.
         * @throws SQLException           if a SQL exception occurs while interacting
         *                                with the database.
         */
        public void addCustomerBtnAction(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_addCustomerBtnAction
                // TODO add your handling code here:

                String id = IdText.getText();
                String firstname = firstNameText.getText();
                String lastname = lastNameText.getText();
                String nic = cnicText.getText();
                String passport = passportText.getText();
                String address = addressText.getText();
                String contact = contactText.getText();
                DateFormat da = new SimpleDateFormat("yyyy-MM-dd");
                // String date = da.format(txtdob.getDate());
                String Gender;

                SimpleDateFormat df = new SimpleDateFormat("yyyy");
                String date = null;
                String year = null;
                int yr = 0;
                try {
                        date = da.format(dateOfBirthText.getDate());
                        year = df.format(dateOfBirthText.getDate());
                        yr = Integer.parseInt(year);
                } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "Please enter the missing fields");
                }

                if (maleRadioBtn.isSelected()) {
                        Gender = "Male";
                } else if (femaleRadioBtn.isSelected()) {
                        Gender = "Female";
                } else
                        Gender = "";
                if (firstname.equals("") || lastname.equals("") || nic.equals("") || passport.equals("")
                                || address.equals("")
                                || date.equals("") || Gender.equals("") || contact.equals("")) {
                        JOptionPane.showMessageDialog(this, "Please enter the missing fields");
                } else if (!firstname.matches("^[A-Za-z]{3,29}$") || !lastname.matches("^[A-Za-z]{3,29}$")) {
                        JOptionPane.showMessageDialog(this, "Please correct Name");
                } else if (!nic.matches("^[0-9]{5}-[0-9]{7}-[0-9]$")) {
                        JOptionPane.showMessageDialog(this, "Please enter cnic in correct format");
                } else if (!passport.matches("^[A-Za-z0-9]{9}$") || !contact.matches("^[0-9]{4}-[0-9]{7}$")) {
                        JOptionPane.showMessageDialog(this, "Please enter in correct format");
                } else if (yr > 2004 || yr < 1922) {
                        JOptionPane.showMessageDialog(this, "Under 18 and above 100 not allowed");
                } else if (userimage == null || userimage.length == 0) {
                    JOptionPane.showMessageDialog(this, "Please upload your photo ");
                }else {
                        try {
                                Class.forName("com.mysql.cj.jdbc.Driver");
                                con = DriverManager.getConnection("jdbc:mysql://localhost/airline", "root", "1234");
                                pst = con.prepareStatement(
                                                "insert into customer(id,firstname,lastname,nic,passport,address,dob,gender,contact,photo)values(?,?,?,?,?,?,?,?,?,?)");

                                pst.setString(1, id);
                                pst.setString(2, firstname);
                                pst.setString(3, lastname);
                                pst.setString(4, nic);
                                pst.setString(5, passport);
                                pst.setString(6, address);
                                pst.setString(7, date);
                                pst.setString(8, Gender);
                                pst.setString(9, contact);
                                pst.setBytes(10, userimage);
                                pst.executeUpdate();

                                JOptionPane.showMessageDialog(null, "Registration Created");
                                AddBtn.setEnabled(false);
                        } catch (ClassNotFoundException ex) {
                                Logger.getLogger(addCustomer.class.getName()).log(Level.SEVERE, null, ex);
                        } catch (SQLException ex) {
                                Logger.getLogger(addCustomer.class.getName()).log(Level.SEVERE, null, ex);
                        }
                }
                AddBtn.setEnabled(false);
        }// GEN-LAST:event_addCustomerBtnAction

