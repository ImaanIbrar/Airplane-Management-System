        /**
         * Event handler for the "Search" button. Retrieves customer information from
         * the database based on the entered customer ID.
         * Displays the retrieved information in the form for editing.
         *
         * @param evt the ActionEvent triggered by the button click
         * @throws ClassNotFoundException if the MySQL JDBC driver class is not found
         * @throws SQLException           if a database access error occurs
         * @throws ParseException         if date parsing fails
         */

        private void searchBtnAction(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_searchBtnAction
                id = txtCustomertId.getText();

                try {
                        con = connectionManager.getConnection();
                        pst = con.prepareStatement("select * from customer where id = ?");
                        pst.setString(1, id);
                        ResultSet rs = pst.executeQuery();

                        if (rs.next() == false) {
                                disableFields();
                                txtfirstname.setText(null);
                                txtlastname.setText(null);
                                txtCnic.setText(null);
                                txtpassport.setText(null);
                                txtaddress.setText(null);
                                txtcontact.setText(null);
                                txtdob.setDate(null);
                                txtPhoto.setIcon(null);

                                JOptionPane.showMessageDialog(this, "Record not Found");
                        } else {
                                browseBtn.setEnabled(true);
                                updateBtn.setEnabled(true);
                                maleRadioBtn.setEnabled(true);
                                femaleRadioBtn.setEnabled(true);
                                txtfirstname.setEditable(true);
                                txtlastname.setEditable(true);
                                txtCnic.setEditable(true);
                                txtpassport.setEditable(true);
                                txtaddress.setEditable(true);
                                txtcontact.setEditable(true);
                                txtdob.setEnabled(true);
                                txtPhoto.setEnabled(true);

                                String fname = rs.getString("firstname");
                                String lname = rs.getString("lastname");
                                String nic = rs.getString("nic");
                                String passport = rs.getString("passport");
                                String address = rs.getString("address");
                                String dob = rs.getString("dob");
                                Date date1 = new SimpleDateFormat("yyyy-MM-dd").parse(dob);
                                String gender = rs.getString("gender");

                                Blob blob = rs.getBlob("photo");
                                _imagebytes = blob.getBytes(1, (int) blob.length());
                                ImageIcon image = new ImageIcon(_imagebytes);
                                Image im = image.getImage();
                                Image myImg = im.getScaledInstance(txtPhoto.getWidth(), txtPhoto.getHeight(),
                                                Image.SCALE_SMOOTH);
                                ImageIcon newImage = new ImageIcon(myImg);

                                if (gender.equals("Female")) {
                                        femaleRadioBtn.setSelected(true);
                                } else {
                                        maleRadioBtn.setSelected(true);
                                }
                                String contact = rs.getString("contact");

                                txtfirstname.setText(fname.trim());
                                txtlastname.setText(lname.trim());
                                txtCnic.setText(nic.trim());
                                txtpassport.setText(passport.trim());
                                txtaddress.setText(address.trim());
                                txtcontact.setText(contact.trim());
                                txtdob.setDate(date1);
                                txtPhoto.setIcon(newImage);
                        }

                } catch (SQLException | ParseException ex) {
                        Logger.getLogger(searchCustomer.class.getName()).log(Level.SEVERE, null, ex);
                }

        }// GEN-LAST:event_searchBtnAction

