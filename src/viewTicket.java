    /**
     * Event handler for the "PROCEED" button.
     * Retrieves a ticket ID from a text field, queries a MySQL database for ticket information,
     * and opens a new billing window (bill class) if the ticket is found.
     * @param evt The ActionEvent generated by the button click.
     * @return None.
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection con;
        PreparedStatement pst;
        String ticketid = txtticket.getText();
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline","root","1234");
            pst = con.prepareStatement("select * from ticket where id = ?");
            pst.setString(1, ticketid);
            ResultSet rs = pst.executeQuery();
            if (!rs.next()) {
                JOptionPane.showMessageDialog(this, "Record not Found");
                } else {
                    String custid=rs.getString("custid");
                    super.dispose();
                    B1=new bill(custid,ticketid);
                    B1.setVisible(true);
                }
        } catch (SQLException excep) {
            excep.printStackTrace();
        } catch (Exception excep) {
            excep.printStackTrace();
        }
    }


